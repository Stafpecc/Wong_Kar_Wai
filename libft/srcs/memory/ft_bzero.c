#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	uint8_t	*str;
	size_t	i;

	i = 0;
	str = (uint8_t *) s;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}
